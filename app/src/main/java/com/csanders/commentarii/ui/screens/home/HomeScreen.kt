@Composable
fun HomeScreen(viewModel: HomeViewModel, onNavigationRequested: (String) -> Unit) {
        Box(
            modifier = Modifier
                .fillMaxSize()
                .semantics { contentDescription = viewModel.semanticName }
        ) {
            val painter = painterResource(id = viewModel.getKenJennings())
            val description = "My favorite forehead"
            ImageCard(
                painter = painter,
                contentDescription = description,
                title = description
            )
        }
    }

@Composable
private fun ImageCard(
    painter: Painter,
    contentDescription: String,
    title: String,
    modifier: Modifier = Modifier
) {
    Card(
        modifier = modifier.fillMaxWidth(),
        shape = RoundedCornerShape(15.dp),
        elevation = CardDefaults.cardElevation(defaultElevation = 5.dp)
    ) {
        Box(modifier = Modifier.fillMaxHeight()) {
            Image(
                painter = painter, contentDescription = contentDescription,
                contentScale = ContentScale.Crop
            )
            Box(
                modifier = Modifier
                    .fillMaxSize()
                    .background(
                        brush = Brush.verticalGradient(
                            colors = listOf(
                                Color.Transparent,
                                Color.Black
                            ), startY = 300f
                        )
                    )
            )
            Box(
                modifier = Modifier
                    .fillMaxSize()
                    .padding(12.dp),
                contentAlignment = Alignment.BottomCenter
            ) {
                Text(text = title, style = TextStyle(color = Color.White, fontSize = 16.sp))
            }
        }
    }
}

